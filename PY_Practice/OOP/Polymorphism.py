# polymorphism describes methods that are able to take on many forms

# 2 type --- dynamic

# dynamic polymorphism --- occurs during runtime of the program
# methods share the same name but have different implementation
# subclass methods will override superclass methods

# statoc polymorphism --- occurs during compile time
# refers to when multiple methods with the same name but different arguments
# are defined in the same class
# also known as method overloading